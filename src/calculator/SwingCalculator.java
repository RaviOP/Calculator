    private void FactorialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FactorialActionPerformed
        if (!"".equals(Screen.getText())){
            if(Screen.getText().contains(".")) {
                double value = Double.parseDouble(Screen.getText());
                int a = (int)value;
                int f=1,i=1;
                for (i=1;i<=a;i++) {
                    f = f * i;
                }
                Screen.setText(f+"");
            } else {
                int a = Integer.parseInt(Screen.getText());
                int f=1,i=1;
                for (i=1;i<=a;i++) {
                    f = f * i;
                }
                Screen.setText(f+"");
            }
        }
    }//GEN-LAST:event_FactorialActionPerformed


    private void EqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EqualsActionPerformed
        num2 = Double.parseDouble(Screen.getText());
        switch (operator) {
            case '+':
                result = num1 + num2;
                Screen.setText(result+"");
                break;
            case '-':
                result = num1 - num2;
                Screen.setText(result+"");
                break;
            case '*':
                result = num1 * num2;
                Screen.setText(result+"");
                break;
            case '/':
                result = num1 / num2;
                Screen.setText(result+"");
                break;
            default:
                break;
        }
    }//GEN-LAST:event_EqualsActionPerformed

